#include "../push_swap.h"

int	argv_is_not_sorted(int argc, int *arr)
{
	int	i;

	i = 0;
	while (i < argc - 2)
	{
		if (arr[i] > arr[i + 1])
			return (1);
		i++;
	}
	return (0);
}
